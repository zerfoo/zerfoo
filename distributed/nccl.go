// Destroy releases the communicator resources.
func (c *NCCLComm) Destroy() error {
	if c == nil || c.handle == 0 {
		return nil
	}
	lib := c.ncclLib

	ret := cuda.Ccall(lib.ncclCommDestroy, c.handle)
	if ret != ncclSuccess {
		return fmt.Errorf("ncclCommDestroy failed: error %d", ret)
	}
	c.handle = 0
	return nil
}

// Rank returns the rank of this communicator.
func (c *NCCLComm) Rank() int { return c.rank }

// NRanks returns the total number of ranks in this communicator.
func (c *NCCLComm) NRanks() int { return c.nRanks }
