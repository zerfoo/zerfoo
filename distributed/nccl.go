// NCCLReduceScatter reduces across ranks and scatters the result.
// recvbuf gets sendcount/nRanks elements per rank. The stream parameter
// is a cudaStream_t (use 0 for the default stream).
func NCCLReduceScatter(comm *NCCLComm, sendbuf, recvbuf unsafe.Pointer, recvcount int, stream uintptr) error {
	if comm == nil {
		return fmt.Errorf("ncclReduceScatter: nil communicator")
	}
	lib := comm.ncclLib

	// ncclReduceScatter(const void* sendbuff, void* recvbuff, size_t recvcount,
	//                    ncclDataType_t datatype, ncclRedOp_t op,
	//                    ncclComm_t comm, cudaStream_t stream)
	ret := cuda.Ccall(lib.ncclReduceScatter,
		uintptr(sendbuf),
		uintptr(recvbuf),
		uintptr(recvcount),
		uintptr(ncclFloat32),
		uintptr(ncclOpSum),
		comm.handle,
		stream,
	)
	if ret != ncclSuccess {
		return fmt.Errorf("ncclReduceScatter failed: error %d", ret)
	}
	return nil
}

